
// regular function
int add(int a, int b) {
  return a + b;
}

// Arrow function
int multiply(int a, intb) => a;

// Anonymous Function
var anonymousFunction = (int a, int b) {
  return a - b;
};

// function with optional parameter
String introduce(String name, {String greeting = 'Hello'}) {
  return '$greeting, $name!';
}
