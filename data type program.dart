
void main() {
  // integer data type (int)
  int age = 30;
  print("age, $age");

  // double
  double pi = 3.142;
  print("pi, $pi");

  // string
  String name = "Festus";
  print("Name, $name");

  // boolean
  bool isRaining = false;
  print("is it raining, $isRaining");

  // list
  List<int> numbers = [10, 22, 30, 50, 99];
  print("Numbers, $numbers");

  // Map
  Map<String, int> ages = {
    "Festus": 22,
    "Kelvin": 23,
    "Dorcas": 20,
    "Jannet": 20,
  };
  print("Ages, $ages");

  // Set data types
  Set<int> uniqueNumbers = {1, 2, 3, 4, 5};
  print("Unique Numbers, $uniqueNumbers");
}

