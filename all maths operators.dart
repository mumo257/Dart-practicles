void main() {
  int a = 10;
  int b = 5;

  // addition
  int sum = a + b;
  print("sum: $sum");

  // subtraction
  int difference = a - b;
  print("difference, $difference");

  // multiplication
  int product = a * b;
  print("product, $product");

  // division
  double division = a / b;
  print("division, $division");

  // modulus
  int rem = a % b;
  print("rem, $rem");
}
